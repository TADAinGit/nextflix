const dateFormatter = (date: Date): string => {
  const options: Intl.DateTimeFormatOptions = {
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
    hour: "2-digit",
    minute: "2-digit",
    second: "2-digit",
    hour12: false,
    timeZone: "UTC",
  };

  return new Intl.DateTimeFormat("vi-VN", options).format(new Date(date));
};

export default dateFormatter;
